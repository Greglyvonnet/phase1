//incrementation_decrementation
// declarer une vriable et affecter une valeur : num1 : 5
//incrementer une variable :
var num1 = 5;
console.log(num1);

num1 = 5 + 1;
console.log(num1);

num1 = num1 + 1;
console.log(num1);

num1 += num1;
console.log(num1);

num1 += 1;
console.log(num1);

num1++;
console.log(num1);

++num1;
console.log(num1);

//decrementer une variable :
num1 = num1 - 1;
console.log(num1);

num1 -= 1;
console.log(num1);

num1--;
console.log(num1);

--num1;
console.log(num1);
